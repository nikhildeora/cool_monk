const getdiv10Data = (data)=>{

    let append_div=document.querySelector(".swiper-wrapper")
    
    append_div.innerHTML=null
    //console.log(data)

    data.forEach(function(el){
        //console.log(el)
        let swiper_slide=document.createElement("div")
        let img_box1=document.createElement("div")
        

        let img = document.createElement("img")

        let description = document.createElement("p")

        let price = document.createElement("h4")

        let rating = document.createElement("p")

        let msg = document.createElement("a")

        img.src = el.img_url

        description.innerText = el.description

        price.innerText = el.price

        rating.innerText = el.rating

        msg.innerHTML = "Free Shipping with $99 order"

        img_box1.append(img,description,price,rating,msg)

        

        swiper_slide.append(img_box1)

        swiper_slide.setAttribute("class","swiper-slide")

        img_box1.setAttribute("class","img-box")

        append_div.append(swiper_slide)

        


    })
}

